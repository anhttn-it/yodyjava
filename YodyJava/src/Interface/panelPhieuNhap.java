    private void btnXoaPNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaPNActionPerformed
        // TODO add your handling code here:
        String ma=txtMaPN.getText();
        try{
            if(ma.isEmpty()){
                JOptionPane.showMessageDialog(this,"Hãy chọn sản phẩm cần xóa!");
                return;
            }
            int mpn=Integer.parseInt(ma);
            if(JOptionPane.showConfirmDialog(this,"Bạn có chắc chắn muốn xóa không!")==JOptionPane.YES_OPTION){
                pn.deleteData(mpn);
                ClearData();
                ShowData();
                setnull();
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,"Xóa thất bại");
        }
    }//GEN-LAST:event_btnXoaPNActionPerformed

    private void btnLuuPNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuPNActionPerformed
        // TODO add your handling code here:
        Object manccObj = cbMaNCC.getSelectedItem();
        Object manvObj = cbMaNV.getSelectedItem();
        String ghichu = txtGhiChu.getText().trim();
        String maphieu=txtMaPN.getText();
        Object mamhObj = cbMaMH.getSelectedItem();
        if(manccObj == null || manvObj == null||mamhObj == null){
            JOptionPane.showMessageDialog(this,"Vui lòng điền đầy đủ thông tin!");
        }

        else{
            try {
                int mamh = (int) mamhObj;
                int mancc = (int) manccObj;
                int manv = (int) manvObj;
                PhieuNhap obj = new PhieuNhap();
                java.util.Date now = new java.util.Date();
                obj.setMaMuaHang(mamh);
                obj.setNgayNhap(now);   // tự động lấy thời gian hiện tại
                //            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                //            txtNgayNhap.setText(sdf.format(now));
                obj.setMaNCC(mancc);
                obj.setMaNV(manv);
                obj.setTongTien(0);
                obj.setGhiChu(ghichu);
                if(cothem==true){
                    pn.InsertData(obj);
                }
                else{
                    int maphieunhap = Integer.parseInt(maphieu);
                    obj.setMaPhieuNhap(maphieunhap);
                    pn.EditData(obj);
                }
                ClearData();
                ShowData();
                setnull();
                setbutton(true);
                setKhoa(true);
                cothem=false;
                tblPN.setEnabled(true);
            } catch(Exception ex){
                JOptionPane.showMessageDialog(this,"Cập nhật thất bại");
            }
        }
    }//GEN-LAST:event_btnLuuPNActionPerformed

