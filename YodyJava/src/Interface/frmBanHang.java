    private void tblbanhangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblbanhangMouseClicked
        try {
            int row = tblbanhang.getSelectedRow();
            String ma = tblbanhang.getValueAt(row, 0).toString();
            mabh = Integer.parseInt(ma);
            BanHang obj = bh.getBanHang(mabh);
            if (obj != null) {
                txtMabh.setText(String.valueOf(obj.getMaBanHang()));
                txtNgaydat.setText(new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(obj.getNgayDatHang()));
                cbMakh.setSelectedItem(obj.getMaKhachHang());
                cbMaNV.setSelectedItem(obj.getMaNV());
                txtGhiChu.setText(obj.getGhiChu());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi đọc dữ liệu: " + e.getMessage());
        }
    }//GEN-LAST:event_tblbanhangMouseClicked

