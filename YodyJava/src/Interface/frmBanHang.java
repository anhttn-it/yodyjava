    // ===================== LOAD COMBOBOX =====================
    public void loadComboBoxMaKH() {
        try {
            cbMakh.removeAllItems();
            List<Integer> list = bh.getAllKH();
            for (Integer maKH : list) {
                cbMakh.addItem(maKH);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi nạp danh sách KH: " + e.getMessage());
        }
    }

    public void loadComboBoxMaNV() {
        try {
            cbMaNV.removeAllItems();
            List<Integer> list = bh.getAllMNV();
            for (Integer maNV : list) {
                cbMaNV.addItem(maNV);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi nạp danh sách NV: " + e.getMessage());
        }
    }

    // ===================== XỬ LÝ GIAO DIỆN =====================
    public void setNull() {
        txtMabh.setText("");
        txtNgaydat.setText("");
        txtGhiChu.setText("");
        cbMakh.setSelectedItem(null);
        cbMaNV.setSelectedItem(null);
        mabh = -1;
    }

    public void setButton(boolean a) {
        btnThem.setEnabled(a);
        btnSua.setEnabled(a);
        btnXoa.setEnabled(a);
        btnLuu.setEnabled(!a);
        btnKLuu.setEnabled(!a);
    }

    public void setKhoa(boolean a) {
        txtGhiChu.setEditable(!a);
        cbMakh.setEnabled(!a);
        cbMaNV.setEnabled(!a);
    }

    public void showData() throws SQLException {
        ClearData();
        List<BanHang> list = bh.getAllBanHang();
        for (BanHang b : list) {
            Object[] row = new Object[]{
                b.getMaBanHang(),
                new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(b.getNgayDatHang()),
                b.getMaKhachHang(),
                b.getMaNV(),
                b.getTongTien(),
                b.getGhiChu()
            };
            tableModel.addRow(row);
        }
    }

    public void ClearData() {
        int n = tableModel.getRowCount() - 1;
        for (int i = n; i >= 0; i--) tableModel.removeRow(i);
    }

