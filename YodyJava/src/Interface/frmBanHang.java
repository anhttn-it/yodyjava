    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if (mabh == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn đơn bán cần xóa!");
            return;
        }
        bh.setMaBanHang(mabh);
        if (JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa không?") == JOptionPane.YES_OPTION) {
            if (bh.deleteBanHang()) {
                JOptionPane.showMessageDialog(this, "Xóa thành công!");
                setNull();
                try { showData(); } catch (SQLException ex) {}
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại!");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        try {
        Object maKHObj = cbMakh.getSelectedItem();
        Object maNVObj = cbMaNV.getSelectedItem();
        if (maKHObj == null || maNVObj == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin!");
            return;
        }
        int maKH = (int) maKHObj;
        int maNV = (int) maNVObj;
        String ghichu = txtGhiChu.getText().trim();

        bh.setMaKhachHang(maKH);
        bh.setMaNV(maNV);
        bh.setGhiChu(ghichu);
        bh.setTongTien(0);
        bh.setNgayDatHang(new Timestamp(System.currentTimeMillis()));

        boolean kq;
        if (cothem) {
            // Khi thêm mới, KHÔNG set MaBanHang
            kq = bh.addBanHang();
        } else {
            // Khi sửa, set MaBanHang để update
            bh.setMaBanHang(mabh);
            kq = bh.updateBanHang();
        }

        if (kq) {
            JOptionPane.showMessageDialog(this, "Lưu thành công!");
            setNull();
            setButton(true);
            setKhoa(true);
            tblbanhang.setEnabled(true);
            cothem = false;
            showData();
        } else {
            JOptionPane.showMessageDialog(this, "Lưu thất bại!");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage());
    }
    }//GEN-LAST:event_btnLuuActionPerformed

