    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        String keyword = txtTK.getText().trim();
        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã bán hàng để tìm!");
            return;
        }
        try {
            ClearData();
            int searchId = Integer.parseInt(keyword);
            BanHang obj = bh.getBanHang(searchId);
            if (obj != null) {
                Object[] row = new Object[]{
                    obj.getMaBanHang(),
                    new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(obj.getNgayDatHang()),
                    obj.getMaKhachHang(),
                    obj.getMaNV(),
                    obj.getTongTien(),
                    obj.getGhiChu()
                };
                tableModel.addRow(row);
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy đơn bán!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi tìm kiếm: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTimActionPerformed

